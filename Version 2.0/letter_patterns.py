"""
8x8 canonical patterns for categories B, digit 0, and E.
Black (stroke) pixels = -1.0, white (background) pixels = +1.0 per assignment spec.
"""
import numpy as np

# Class indices: 0 = B, 1 = 0 (zero), 2 = E
CLASS_NAMES = ("B", "0", "E")


